package entity;

public class ResourceInfo {
   private Integer resource_id;
   private String resource_name;
   private String resource_type;
   private String resource_http_path;
   private String resource_disk_path;
public Integer getResource_id() {
	return resource_id;
}
public void setResource_id(Integer resource_id) {
	this.resource_id = resource_id;
}
public String getResource_name() {
	return resource_name;
}
public void setResource_name(String resource_name) {
	this.resource_name = resource_name;
}
public String getResource_type() {
	return resource_type;
}
public void setResource_type(String resource_type) {
	this.resource_type = resource_type;
}
public String getResource_http_path() {
	return resource_http_path;
}
public void setResource_http_path(String resource_http_path) {
	this.resource_http_path = resource_http_path;
}
public String getResource_disk_path() {
	return resource_disk_path;
}
public void setResource_disk_path(String resource_disk_path) {
	this.resource_disk_path = resource_disk_path;
}
   
   
   
}
